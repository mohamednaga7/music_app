<template>
  <main>
    <!-- Music Header -->
    <section class="w-full mb-8 py-14 text-center text-white relative">
      <div
        class="absolute inset-0 w-full h-full box-border bg-contain music-bg"
        style="background-image: url(/assets/img/song-header.png)"
      ></div>
      <div class="container mx-auto flex items-center">
        <!-- Play/Pause Button -->
        <button
          @click.prevent="newSong(song!)"
          type="button"
          class="z-50 h-24 w-24 text-3xl bg-white text-black rounded-full focus:outline-none"
        >
          <i
            class="fas"
            :class="{
              'fa-pause': current_song?.docId === song?.docId && playing,
              'fa-play':
                !current_song ||
                !song ||
                current_song.docId !== song.docId ||
                !playing,
            }"
          ></i>
        </button>
        <div class="z-50 text-left ml-8">
          <!-- Song Info -->
          <div class="text-3xl font-bold">{{ song?.modified_name }}</div>
          <div>{{ song?.genre }}</div>
        </div>
      </div>
    </section>
    <!-- Form -->
    <section class="container mx-auto mt-6" id="comments">
      <div
        class="bg-white rounded border border-gray-200 relative flex flex-col"
      >
        <div class="px-6 pt-6 pb-5 font-bold border-b border-gray-200">
          <!-- Comment Count -->
          <span class="card-title"
            >Comments ({{ song?.comment_count || 0 }})</span
          >
          <i class="fa fa-comments float-right text-green-400 text-2xl"></i>
        </div>
        <div class="p-6">
          <div
            class="text-white text-center font-bold p-4 mb-4"
            v-if="comment_show_alert"
            :class="comment_alert_variant"
          >
            {{ comment_alert_message }}
          </div>
          <vee-form
            :validation-schema="schema"
            @submit="addComment"
            v-if="userLoggedIn"
          >
            <vee-field
              as="textarea"
              name="comment"
              class="block w-full py-1.5 px-3 text-gray-800 border border-gray-300 transition duration-500 focus:outline-none focus:border-black rounded mb-4"
              placeholder="Your comment here..."
            ></vee-field>
            <ErrorMessage class="text-red-600" name="comment" />
            <button
              type="submit"
              class="py-1.5 px-3 rounded text-white bg-green-600 block"
              :disabled="comment_in_submission"
            >
              Submit
            </button>
          </vee-form>
          <!-- Sort Comments -->
          <select
            v-model="sort"
            class="block mt-4 py-1.5 px-3 text-gray-800 border border-gray-300 transition duration-500 focus:outline-none focus:border-black rounded"
          >
            <option value="1">Latest</option>
            <option value="2">Oldest</option>
          </select>
        </div>
      </div>
    </section>
    <!-- Comments -->
    <ul class="container mx-auto">
      <li
        v-for="comment in comments"
        :key="comment.docId"
        class="p-6 bg-gray-50 border border-gray-200"
      >
        <!-- Comment Author -->
        <div class="mb-5">
          <div class="font-bold">{{ comment.name }}</div>
          <time>{{ comment.readableDate }}</time>
        </div>

        <p>
          {{ comment.content }}
        </p>
      </li>
    </ul>
  </main>
</template>

<script lang="ts">
import { auth, songsCollection, commentsCollection } from '@/includes/firebase';
import type { Song } from '@/types/Song';
import { mapActions, mapState } from 'pinia';
import { defineComponent } from 'vue';
import useUserStore from '@/stores/user';
import { usePlayerStore } from '@/stores/player';
import moment from 'moment';

import type { Comment } from '@/types/Comment';

export default defineComponent({
  name: 'AppSong',
  data(): {
    song: Song | null;
    schema: { comment: string };
    comments: Comment[];
    comment_in_submission: boolean;
    comment_show_alert: boolean;
    comment_alert_variant: string;
    comment_alert_message: string;
    sort: '1' | '2';
  } {
    return {
      song: null,
      schema: {
        comment: 'required|min:3',
      },
      comments: [],
      comment_in_submission: false,
      comment_show_alert: false,
      comment_alert_variant: 'bg-blue-500',
      comment_alert_message: 'Please wait! Your comment is being submitted',
      sort: '1',
    };
  },
  methods: {
    ...mapActions(usePlayerStore, ['newSong']),
    async addComment(
      values: { comment: string },
      { resetForm }: { resetForm: () => void }
    ) {
      if (auth.currentUser && auth.currentUser.uid) {
        this.comment_in_submission = true;
        this.comment_show_alert = true;
        this.comment_alert_variant = 'bg-blue-500';
        this.comment_alert_message =
          'Please wait! Your comment is being submitted';
        const comment: Comment = {
          content: values.comment,
          datePosted: new Date().getTime(),
          sid: this.$route.params.id as string,
          name: auth.currentUser.displayName || '',
          uid: auth.currentUser.uid,
        };
        await commentsCollection.add(comment);

        if (this.song) {
          this.song.comment_count += 1;
          await songsCollection.doc(this.$route.params.id as string).update({
            comment_count: this.song.comment_count,
          });
        }

        this.comment_in_submission = false;
        this.comment_alert_variant = 'bg-green-500';
        this.comment_alert_message = 'Comment added!';

        resetForm();
      }
    },
    async getComments() {
      const snapshots = await commentsCollection
        .orderBy('datePosted', this.sort === '1' ? 'desc' : 'asc')
        .where('sid', '==', this.$route.params.id)
        .get();

      this.comments = [];

      snapshots.forEach((doc) => {
        const data = doc.data() as Comment;
        this.comments.push({
          docId: doc.id,
          ...data,
          readableDate: moment(data.datePosted).format('DD/MM/YYYY HH:mm'),
        });
      });

      commentsCollection
        .orderBy('datePosted', this.sort === '1' ? 'desc' : 'asc')
        .where('sid', '==', this.$route.params.id)
        .onSnapshot((querySnapshot) => {
          this.comments = [];

          querySnapshot.forEach((doc) => {
            const data = doc.data() as Comment;
            this.comments.push({
              docId: doc.id,
              ...data,
              readableDate: moment(data.datePosted).format('DD/MM/YYYY HH:mm'),
            });
          });
        });
    },
  },
  computed: {
    ...mapState(useUserStore, ['userLoggedIn']),
    ...mapState(usePlayerStore, ['current_song', 'playing']),
  },
  async created() {
    const docSnapshot = await songsCollection
      .doc(this.$route.params.id as string)
      .get();
    if (!docSnapshot.exists) {
      this.$router.push({ name: 'home' });
      return;
    }
    this.song = docSnapshot.data() as Song;

    const { sort } = this.$route.query;

    this.sort = sort === '1' || sort === '2' ? sort : '1';

    this.getComments();
  },
  watch: {
    sort(newVal) {
      if (newVal === this.$route.query.sort) {
        return;
      }
      this.$router.push({
        query: {
          sort: newVal,
        },
      });

      this.getComments();
    },
  },
});
</script>

